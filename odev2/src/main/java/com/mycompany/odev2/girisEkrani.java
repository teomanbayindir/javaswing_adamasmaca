    private void logYaz(String mesaj){
        try{
            FileWriter fw = new FileWriter(LOG_DOSYASI,true);
            PrintWriter pw = new PrintWriter(fw);
            
            LocalDateTime simdi = LocalDateTime.now();
            DateTimeFormatter format = DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm:ss");
            String tarihSaat = simdi.format(format);
            
            pw.println("["+ tarihSaat +"] "+mesaj);
            pw.close();
        }
        catch(IOException e){
            System.out.println("Log yazılamadı: "+ e.getMessage());
        }
    }
    
    private void baslangicKontrol(){
        File dosya = new File(SIFRE_DOSYASI);
        
        if(dosya.exists() && dosya.length() > 0){
            sifreVarMi = false;
            lblMesaj.setText("Şifre girin: ");
            
            try{
                BufferedReader br= new BufferedReader(new FileReader(dosya));
                kayitliSifre = br.readLine();
                br.close();
            }
            catch(IOException e){
                System.out.println("Hata!!!!!");
            }
        }
        else{
            sifreVarMi = true;
            lblMesaj.setText("Şifre belirlenmemiş!! Aşağıya yazarak şifrenizi oluşturun.");
        }
    }
