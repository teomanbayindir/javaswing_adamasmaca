    private void btnGirisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGirisActionPerformed
        String girilenSifre = new String(txtSifre.getPassword());
        
        if(girilenSifre.equals("")){
            JOptionPane.showMessageDialog(this, "Lütfen bir şifre giriniz!");
            return;
        }
        
        if(sifreVarMi){
            try{
                FileWriter fw = new FileWriter(SIFRE_DOSYASI);
                fw.write(girilenSifre);
                fw.close();
                
                logYaz("Şifre oluşturuldu.");
                JOptionPane.showMessageDialog(this, "Şifre kaydedildi. Giriş yap.");
                
                sifreVarMi = false;
                kayitliSifre = girilenSifre;
                lblMesaj.setText("Şifre girin: ");
                txtSifre.setText("");
            }
            catch(IOException e){
                JOptionPane.showMessageDialog(this, "Hata! Şifre kaydedilemedi.");
            }
        }
        else{
            if(girilenSifre.equals(kayitliSifre)){
                logYaz("Giriş Yapıldı.");
                
               JOptionPane.showMessageDialog(this, "Giriş yapıldı. Oyun açılıyor...");
               oyunEkrani oyun = new oyunEkrani();
               oyun.setVisible(true);
               this.dispose(); // dispose: giriş ekranını kapatma satırı!!! ram'i meşgul etmesin!
            }
            else{
                hataliGirisSayisi++;
                logYaz("Hatalı deneme! (Deneme: "+ hataliGirisSayisi + ")");
                
                if(hataliGirisSayisi>=3){
                    logYaz("Maksimum deneme hakkı! Kapatıldı!");
                    JOptionPane.showMessageDialog(this, "Maksimum denemeye ulaşıldı! Kapatılıyor...");
                    System.exit(0); // programı kapatma satırı 
                }
                else{
                    JOptionPane.showMessageDialog(this, "Şifre hatalı! Kalan deneme: "+ (3-hataliGirisSayisi));
                    txtSifre.setText("");
                }
            }
        }
    }//GEN-LAST:event_btnGirisActionPerformed

